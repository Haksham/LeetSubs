# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def mergeTwoLists(self, list1: Optional[ListNode], list2: Optional[ListNode]) -> Optional[ListNode]:
        one=list1
        two=list2
        ans=ListNode()
        tail=ans
        while one and two:
            if one.val>=two.val:
                tail.next=two
                two=two.next
            else:
                tail.next=one
                one=one.next
            tail=tail.next
        if one:
            tail.next = one
        if two:
            tail.next = two
        return ans.next